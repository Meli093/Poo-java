package Polimorfismo;

/**
 *
 * @author MR
 */
public class FechaCumpleaños {
private int dia;
private int mes;
private int anos;

    public FechaCumpleaños() {
    }
    public FechaCumpleaños(int dia, int mes, int años) {
        this.dia = dia;
        this.mes = mes;
        this.anos = años;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getAnos() {
        return anos;
    }

    public void setAnos(int anos) {
        this.anos = anos;
    }

    
        
} 